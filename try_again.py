def leet_conver(word):
    if re.match(r'^(?!\d+$)', word):
        word = str(word)
        word = word.lower()
        newval = ""
        for char in word:

            if char in leet:
                newval += leet[char]

            else:
                newval += char
        return (newval)
    else:
        return word


